export function splitKeywords(keywords, whiteCount = 4, blackCount = 4) {
  const shuffled = keywords.sort(() => Math.random() - 0.5);
  return {
    white: shuffled.slice(0, whiteCount),
    black: shuffled.slice(whiteCount, whiteCount + blackCount)
  };
}